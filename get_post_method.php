<h5>What are "GET" and "POST"?</h5>
<p>GET and POST are methods used to send data to the server: With the GET method, the browser appends the data onto the URL. With the Post method, the data is sent as "standard input."</p>
<hr/>

<h5>GET</h5>
<p>When we submit a form, which has the GET method it displays pair of name/value used in the form at the address bar of the browser preceded by url. </p>
<p>When you want to send short or small data , then you can use GET Method.</p>
<p>E.g. http://localhost/plan?name=rajesh&email=rajesh@gmail.com</p>
<hr/>

<h5>POST</h5>
<p>Post method doesn't display these values.  </p>
<p>Long data sending, say more then 100 character you can use POST method. </p>
<p>E.g. http://localhost/plan</p>
